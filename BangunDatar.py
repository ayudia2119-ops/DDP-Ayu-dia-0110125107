def jajar_genjang (alas, tinggi):
    return alas * tinggi

def persegi_panjang (p, l):
    return p * l

def persegi (sisi):
    return sisi ** 3

def segitiga (alas, tinggi):
    return alas * tinggi / 2

def lingkaran (jari_jari):
    return 3.14 * jari_jari

print(persegi_panjang(4,6))
print(persegi(4))
print(segitiga(4,6))
print(lingkaran(7))
print(jajar_genjang(5,8))